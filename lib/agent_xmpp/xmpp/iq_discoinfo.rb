    class IqDiscoInfo < IqQuery

      #.........................................................................................................
      name_xmlns 'query', 'http://jabber.org/protocol/disco#info'

      #####-------------------------------------------------------------------------------------------------------
      class << self

        #.........................................................................................................
        def get(to, pipe)
          iq = Iq.new(:get, to)
          query = IqDiscoInfo.new
          iq.add(query)
          Send(iq) do |r|
            if (r.type == :result) && r.query.kind_of?(Xmpp::IqDiscoInfo)
              pipe.broadcast_to_delegates(:did_receive_discoinfo_result, pipe, r.from, r.query)
            end
          end
        end
        
        #.........................................................................................................
        def result(request, pipe)
          iq = Xmpp::Iq.new(:result, request.from.to_s)
          iq.id = request.id unless request.id.nil?
          iq.query = IqDiscoInfo.new
          Send(iq)
        end
        
      #### self  
      end
      
      #.........................................................................................................
      def node
        attributes['node']
      end

      #.........................................................................................................
      def node=(val)
        attributes['node'] = val
      end

      #.........................................................................................................
      def set_node(val)
        self.node = val
        self
      end

      #.........................................................................................................
      def identity
        first_element('identity')
      end

      #.........................................................................................................
      def identities
        get_elements('identity')
      end

      #.........................................................................................................
      def features
        elements.inject('feature', []) { |r, f| r.push(f.var)}
      end
  
    #### IqDiscoInfo
    end

