module AgentXmpp
  
  #####-------------------------------------------------------------------------------------------------------
  class PublishedModel

    #.........................................................................................................
    def initialize(items)
      @items = items
    end

    #.........................................................................................................
    def find_all
      @items.collect{|i| PublishedItem.new(i)}
    end

  #### PublishedModel
  end

  #####-------------------------------------------------------------------------------------------------------
  class PublishedItem

    #.........................................................................................................
    attr_reader :node, :description
      
    #.........................................................................................................
    def initialize(pub)
      @node = pub['node']
      @description = pub['description']
    end

  #### PublishedItem
  end

#### AgentXmpp
end
